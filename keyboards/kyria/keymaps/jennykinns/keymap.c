/*
 * The `LAYOUT_kyria_base` macro is a template to allow the use of identical
 * modifiers for the default layouts (eg QWERTY, Colemak, Dvorak, etc), so
 * that there is no need to set them up for each layout, and modify all of
 * them if I want to change them.  This helps to keep consistency and ease
 * of use. K## is a placeholder to pass through the individual keycodes
 *
 *
 * ,-------------------------------------------.                              ,-------------------------------------------.
 * |  Tab   |      |      |      |      |      |                              |      |      |      |      |      |   ?    |
 * |--------+------+------+------+------+------|                              |------+------+------+------+------+--------|
 * |  Enter |      |      |      |      |      |                              |      |      |      |      |      | Enter  |
 * |--------+------+------+------+------+------+-------------.  ,-------------+------+------+------+------+------+--------|
 * |  Shift |      |      |      |      |      | BkSp | Esc  |  | Esc  | Bksp |      |      |      |      |      |   ?    |
 * `----------------------+------+------+------+------+------|  |------+------+------+------+------+----------------------'
 *                        |KeyLck| Alt  |Raise |Space | Ctrl |  | Ctrl |Space |Lower |  ?   |Leader|
 *                        `----------------------------------'  `----------------------------------'
 */
#define LAYOUT_kyria_base( \
    K01, K02, K03, K04, K05, K06, K07, K08, K09, K0A, \
    K11, K12, K13, K14, K15, K16, K17, K18, K19, K1A, \
    K21, K22, K23, K24, K25, K26, K27, K28, K29, K2A  \
    ) \
    LAYOUT_wrapper( \
      KC_TAB,  K01,     K02,     K03,     K04,     K05,                                             K06,         K07,     K08,     K09,     K0A,     KC_BSPC, \
      KC_ENT,  K11,     K12,     K13,     K14,     K15,                                             K16,         K17,     K18,     K19,     K1A,     KC_ENT,  \
      KC_LSFT, K21,     K22,     K23,     K24,     K25,         KC_BSPC, KC_ESC,  KC_ESC,  KC_BSPC, K26,         K27,     K28,     K29,     K2A,     XXXXXXX, \
                                 KC_LOCK, KC_LALT, TD(TD_RALO), KC_SPC,  KC_LCTL, KC_RCTL, KC_SPC,  TD(TD_LORA), XXXXXXX, XXXXXXX \
    )
/* Re-pass though to allow templates to be used */
#define LAYOUT_kyria_base_wrapper(...)    LAYOUT_kyria_base(__VA_ARGS__)

